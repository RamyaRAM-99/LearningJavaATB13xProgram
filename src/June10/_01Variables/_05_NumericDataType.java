package June10._01Variables;

public class _05_NumericDataType {
    public static void main(String[] args) {
        byte age = 25;
        byte minValue = -128;
        byte maxValue = 127;


        short salary = 15000;
        short temperature = -200;


        int distance = 100000;
        int population = 1380000000;



        long worldPopulation = 7900000000L; // Use 'L' suffix
        long nationalDebt = 123456789012345L;


        float price = 10.99f; // Use 'f' suffix
        float temperatureToday = -12.45f;


        double pi = 3.141592653589793;
        double bankBalance = 125478.75;


        char grade = 'A';
        char symbol = '#';
        char digitChar = '5';


        boolean isJavaFun = true;
        boolean hasPermission = false;





        byte b = 100;
        short s = 20000;
        int i = 150000;
        long l = 123456789L;
        float f = 99.99f;
        double d = 123456.789;
        char c = 'J';
        boolean isLearning = true;

        System.out.println("byte: " + b);
        System.out.println("short: " + s);
        System.out.println("int: " + i);
        System.out.println("long: " + l);
        System.out.println("float: " + f);
        System.out.println("double: " + d);
        System.out.println("char: " + c);
        System.out.println("boolean: " + isLearning);


    }
}
