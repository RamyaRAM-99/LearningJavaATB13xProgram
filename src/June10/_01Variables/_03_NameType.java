package June10._01Variables;

public class _03_NameType {
    public static void main(String[] args) {
        int _ = 1;
        int $ = 2;
        int abcd = 3;
        int _abcd_ = 4;
        int a_b_c = 5;
        int _123 = 6;
        int totalNumberOfStudentsEnrolledInTheAdvancedJavaCourse = 7;
        int x;                         // Single letter
        int age;                       // Simple and valid
        int studentName;               // Camel case
        int totalMarksObtained;        // Long descriptive name
        int _count;                    // Starts with underscore
        int $amount;                   // Starts with dollar sign (valid, but not preferred)
        int user_id;                   // Snake case (valid, often used in DB code)
        int numberOfStudentsInClass;   // Descriptive and valid
        int MAX_SIZE = 100;            // Constant (usually written in uppercase)
        int a1b2c3;                    // Contains letters and digits

    }
}
